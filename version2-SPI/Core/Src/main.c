void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
    static uint32_t last_tick = 0; 

    if (HAL_GetTick() - last_tick < 200) 
        return;

    last_tick = HAL_GetTick();

    if(GPIO_Pin==BTN_UP_Pin){
        if(num<9) num++;
    } 
    else if(GPIO_Pin==BTN_DOWN_Pin){
        if(num>0) num--;
    }
}


